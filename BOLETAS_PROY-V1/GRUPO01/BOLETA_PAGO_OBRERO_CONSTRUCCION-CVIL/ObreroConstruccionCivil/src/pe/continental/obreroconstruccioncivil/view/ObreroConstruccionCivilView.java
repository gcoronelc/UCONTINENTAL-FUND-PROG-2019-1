    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Datos:
        String categoria = comboCategoria.getSelectedItem().toString();
        String condicion =  comboGratificacion.getSelectedItem().toString();
        String nombre = txtNombre.getText();
        String DNI = txtDNI.getText();
        int cantHijos = Integer.parseInt(txtCantHijos.getText());
        int diasLaborales = Integer.parseInt(txtDiasLaborales.getText());
        int dias60extra =Integer.parseInt(txtHRExtra60.getText());
        int dias100extra = Integer.parseInt(txtHRExtras100.getText());
        // Proceso:
        double RemuneracionTotal = ObreroConstruccionCivilService.determinarSalarioBasico(categoria, diasLaborales);
        double Dominical = ObreroConstruccionCivilService.determinarDominical(categoria, diasLaborales);
        double BUC = ObreroConstruccionCivilService.determinarBUC(categoria, diasLaborales);
        double Movilidad = ObreroConstruccionCivilService.determinarMovilidad(diasLaborales);
        double Asignacion = ObreroConstruccionCivilService.determinarAsignacion(categoria, cantHijos, diasLaborales);
        double HRExtra60 = ObreroConstruccionCivilService.Pago60Extra(categoria, diasLaborales, dias60extra);
        double HRExtra100 = ObreroConstruccionCivilService.Pago100Extra(categoria, diasLaborales, dias100extra);
        double Vacaciones = ObreroConstruccionCivilService.determinarVacaciones(categoria, diasLaborales);
        double Indemnizacion = ObreroConstruccionCivilService.determinarIndemnizacion(categoria, diasLaborales);
        double Gratificacion = ObreroConstruccionCivilService.determinarGratificacion(categoria, condicion, diasLaborales);
        double Conafovicer = ObreroConstruccionCivilService.determinarCONAFOVICER(categoria, diasLaborales);
        double ONP = ObreroConstruccionCivilService.determinarONP(categoria, diasLaborales);
        double ImporteTotal = ObreroConstruccionCivilService.ImporteTotal(categoria, diasLaborales, cantHijos, dias60extra, dias100extra, condicion);
        // Reporte
        txtRemuneracionBasica.setText("" + RemuneracionTotal);
        txtDominical.setText("" + Dominical);
        txtBUC.setText("" + BUC);
        txtMovilidad.setText("" + Movilidad);
        txtAsignacion.setText("" + Asignacion);
        txtRepoHRExtras60.setText("" + HRExtra60);
        txtRepoHRExtras100.setText("" + HRExtra100);
        txtVacaciones.setText("" + Vacaciones);
        txtIndemnizacion.setText("" + Indemnizacion);
        txtGratificacion.setText("" + Gratificacion);
        txtConafovicer.setText("" + Conafovicer);
        txtONP.setText("" + ONP);
        txtImporteTotal.setText("" + ImporteTotal);
        txtRemuneracionBasica.setText("" + RemuneracionTotal);
        
    }//GEN-LAST:event_btnCalcularActionPerformed

