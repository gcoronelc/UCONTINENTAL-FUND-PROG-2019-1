
package pe.continental.supermoda.dto;

public class SuperModaDto {
    // Datos
    private double lote;
    private double precioLote;
    private double gastosAdministrativos;
    private double gananciaUnidad;
    
    // Resultados
    private double unidadesComercializar;
    private double costoTotalLote;
    private double costoUnidad;
    private double precioVenta;
    private double precioPublico;

    public double getLote() {
        return lote;
    }

    public void setLote(double lote) {
        this.lote = lote;
    }

    public double getPrecioLote() {
        return precioLote;
    }

    public void setPrecioLote(double precioLote) {
        this.precioLote = precioLote;
    }

    public double getGastosAdministrativos() {
        return gastosAdministrativos;
    }

    public void setGastosAdministrativos(double gastosAdministrativos) {
        this.gastosAdministrativos = gastosAdministrativos;
    }

    public double getGananciaUnidad() {
        return gananciaUnidad;
    }

    public void setGananciaUnidad(double gananciaUnidad) {
        this.gananciaUnidad = gananciaUnidad;
    }

    public double getUnidadesComercializar() {
        return unidadesComercializar;
    }

    public void setUnidadesComercializar(double unidadesComercializar) {
        this.unidadesComercializar = unidadesComercializar;
    }

    public double getCostoTotalLote() {
        return costoTotalLote;
    }

    public void setCostoTotalLote(double costoTotalLote) {
        this.costoTotalLote = costoTotalLote;
    }

    public double getCostoUnidad() {
        return costoUnidad;
    }

    public void setCostoUnidad(double costoUnidad) {
        this.costoUnidad = costoUnidad;
    }

    public double getPrecioVenta() {
        return precioVenta;
    }

    public void setPrecioVenta(double precioVenta) {
        this.precioVenta = precioVenta;
    }

    public double getPrecioPublico() {
        return precioPublico;
    }

    public void setPrecioPublico(double precioPublico) {
        this.precioPublico = precioPublico;
    }

    
    
}
